export class Game {
  width: number;
  height: number;
  currLevel: GameLevel;
  gameLevels: GameLevel[];
  players: Player[] = [];
  player: Player;
  isInit: boolean = false;
  constructor() {}

  drawBg(image: Sprite["image"]) {
    const bg = new Sprite({ image, position: { x: 0, y: 0 } });
    bg.draw();
  }

  getGameLevels(levelsData: GameLevelData[]) {
    const levels: GameLevel[] = [];
    levelsData.forEach((item) => {
      const level = new GameLevel(item);
      levels.push(level);
    });
    return levels;
  }

  switchLevel(levelNo: number) {
    this.isInit = false;
    //find first level to render
    const gameLevel = this.gameLevels.find((level) => level.no == levelNo);

    if (gameLevel) {
      gsap.fromTo(
        "body",
        {
          css: {
            opacity: 0,
          },
        },
        {
          duration: 1,
          css: {
            opacity: 1,
          },
        }
      );

      const isNextLevel = !this.currLevel || gameLevel.no >= this.currLevel?.no;

      this.player.position = {
        x: isNextLevel ? gameLevel.door.prev.x : gameLevel.door.next.x,
        y: isNextLevel ? gameLevel.door.prev.y : gameLevel.door.next.y,
      };
      Global.canvas.width = gameLevel.width;
      Global.canvas.height = gameLevel.height;
      this.players = [this.player];
      gameLevel.bots.forEach((bot) => {
        this.addPlayer(bot);
      });
      gameLevel.draw();
      gameLevel.drawCollisionBlocks();
      this.currLevel = gameLevel;
      this.isInit = true;
    }
  }

  addPlayer(player: Player) {
    this.players.push(player);
  }
  removePlayer(player: Player) {
    this.players.remove(player.id, "id");
  }

  refreshBg() {
    Global.ctx.fillStyle = "black";
    Global.ctx.fillRect(0, 0, Global.canvas.width, Global.canvas.height);
  }

  update() {
    requestAnimationFrame(() => this.update());

    const centerBodyX = Global.body.clientWidth / 2;
    const centerBodyY = Global.body.clientHeight / 2;

    const directionX =
      centerBodyX -
      ((Global.body.clientWidth - Global.canvas.width) / 2 +
        this.player.side.centerX);

    const directionY =
      centerBodyY -
      ((Global.body.clientHeight - Global.canvas.height) / 2 +
        this.player.side.centerY);

    gsap.to("#game", {
      duration: 1,
      css: {
        transform: `translate(${directionX}px,${directionY}px)`,
      },
    });

    if (this.isInit) {
      if (this.player.side.l > Global.canvas.width) {
        this.switchLevel(
          Math.min(this.currLevel.no + 1, this.gameLevels.length)
        );
      } else if (this.player.side.r < 0) {
        this.switchLevel(this.currLevel.no - 1);
      } else {
        this.refreshBg();

        this.players.forEach((player) => {
          this.currLevel.draw();
          this.currLevel.drawCollisionBlocks();
          player.update(this.currLevel.collisions);
          player.draw();
        });
      }
    }
  }

  async init() {
    //INIT GAME HERE

    //create game level instance from level object data
    const gameLevels = this.getGameLevels(levels);

    this.gameLevels = gameLevels;

    //switch to selected level
    const firstPlayer = new Player({
      id: v4(),
      bodyPartName: "knight-3",
      size: {
        w: 50,
        h: 70,
      },
    });

    this.player = firstPlayer;

    GameKeyEvent.move(firstPlayer);

    this.addPlayer(firstPlayer);

    this.switchLevel(FIRST_LEVEL_NO);

    //add first player to testing

    this.update();
  }
}
